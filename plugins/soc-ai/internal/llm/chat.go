// ChatMessage is a single turn in a conversation.
type ChatMessage struct {
	Role    string `json:"role"`    // "system" | "user" | "assistant"
	Content string `json:"content"`
}

// ChatContext carries optional page context from the frontend.
type ChatContext struct {
	CurrentAlertID    string         `json:"currentAlertId,omitempty"`
	CurrentIncidentID string         `json:"currentIncidentId,omitempty"`
	RecentAlerts      []interface{}  `json:"recentAlerts,omitempty"`
	TimeRange         string         `json:"timeRange,omitempty"`
}

// ChatRequest is the body of POST /api/v1/chat.
type ChatRequest struct {
	Messages []ChatMessage `json:"messages"`
	Context  *ChatContext  `json:"context,omitempty"`
	Stream   bool          `json:"stream"`
}

// ChatDelta is a single SSE payload chunk.
type ChatDelta struct {
	Delta string `json:"delta,omitempty"`
	Done  bool   `json:"done,omitempty"`
	Error string `json:"error,omitempty"`
}

// openAIStreamChunk is the SSE data shape from OpenAI-compatible streaming APIs.
type openAIStreamChunk struct {
	Choices []struct {
		Delta struct {
			Content string `json:"content"`
		} `json:"delta"`
		FinishReason string `json:"finish_reason"`
	} `json:"choices"`
}

// anthropicStreamEvent is the SSE data shape from Anthropic streaming API.
type anthropicStreamEvent struct {
	Type  string `json:"type"`
	Delta *struct {
		Type string `json:"type"`
		Text string `json:"text"`
	} `json:"delta,omitempty"`
}

